public class Hale_Variable2
{
	public static void main(String[] args)
	{
		
		
		double doraMoney;
		double bootsMoney;
		
		doraMoney = 23.11;
		bootsMoney = 66.77;
		
		System.out.println("Dora's cash = " + "\t\t" + doraMoney);
		
		System.out.println("Boots' cash = " + "\t\t" + bootsMoney);
		
		System.out.println("Dora and Boots = " + "\t" + (doraMoney + bootsMoney));
		
		
		
		
	}
	
}
		